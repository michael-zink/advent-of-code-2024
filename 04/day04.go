func part1(input []string) {
	result := 0

	runes := make([][]rune, len(input))
	for i, line := range input {
		runes[i] = []rune(line)
	}

	for x := 0; x < len(input[0]); x++ {
		for y := 0; y < len(input); y++ {
			for shiftX := -1; shiftX <= 1; shiftX++ {
				for shiftY := -1; shiftY <= 1; shiftY++ {
					result += findWord(runes, "XMAS", x, y, shiftX, shiftY)
				}
			}
		}
	}

	fmt.Println("Part 1: ", result)
}

func getChar(input [][]rune, x, y int) rune {
	if y >= 0 && y < len(input) && x >= 0 && x < len(input[y]) {
		return input[y][x]
	}
	return ' '
}

func findWord(input [][]rune, word string, x, y, shiftX, shiftY int) int {
	for _, character := range word {
		if getChar(input, x, y) != character {
			return 0
		}
		x += shiftX
		y += shiftY
	}

	return 1
}

func part2(input []string) {
	result := 0

	runes := make([][]rune, len(input))
	for i, line := range input {
		runes[i] = []rune(line)
	}

	for x := 0; x < len(input[0]); x++ {
		for y := 0; y < len(input); y++ {
			result += findX_MAS(runes, x, y)
		}
	}

	fmt.Println("Part 2: ", result)
}

func findX_MAS(symbols [][]rune, x int, y int) int {
	if getChar(symbols, x, y) != 'A' {
		return 0
	}

	s1 := getChar(symbols, x-1, y-1)
	s2 := getChar(symbols, x+1, y+1)
	str := string([]rune{s1, s2})

	if str != "MS" && str != "SM" {
		return 0
	}

	s1 = getChar(symbols, x-1, y+1)
	s2 = getChar(symbols, x+1, y-1)

	str = string([]rune{s1, s2})

	if str != "MS" && str != "SM" {
		return 0
	}

	return 1
}
